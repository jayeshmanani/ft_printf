char	*ft_strchr(const char *s, int c)
{
	int	ind;

	ind = 0;
	while (s[ind] != 0)
	{
		if (s[ind] == c)
		{
			return (&s[ind]);
		}
		ind++;
	}
	if (s[ind] == 0 && c != 0)
		return (0);
    return (&s[ind]);
}
