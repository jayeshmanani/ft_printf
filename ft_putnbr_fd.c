#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	num;
	char	print_char;

	num = n;
	if (num < 0)
	{
		write(fd, "-", 1);
		num = -num;
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	print_char = (num % 10) + 48;
	write(fd, &print_char, 1);
}
